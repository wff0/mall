package com.wff.mall.product.app;

import com.wff.common.utils.PageUtils;
import com.wff.common.utils.R;
import com.wff.common.valid.AddGroup;
import com.wff.common.valid.UpdateGroup;
import com.wff.common.valid.UpdateStatus;
import com.wff.mall.product.entity.BrandEntity;
import com.wff.mall.product.service.BrandService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.validation.annotation.Validated;
import org.springframework.web.bind.annotation.*;

import java.util.Arrays;
import java.util.List;
import java.util.Map;


